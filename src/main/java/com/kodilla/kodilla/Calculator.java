package com.kodilla.kodilla;

public class Calculator {

    int a;
    int b;
    int result;

    public Calculator(int a, int b)
    {
        this.a = a;
        this.b = b;

    }

    public int addAToB()
    {
        return result = a + b;
    }

    public int aMinusB()
    {
        return result = a - b;
    }
}
